      SUBROUTINE OPINCR(ERROR,*)

C#### Subroutine: OPINCR
C###  Description:
C###    OPINCR outputs increment vectors.

      IMPLICIT NONE
!     Parameter List
      CHARACTER ERROR*(*)
!     Local Variables

      CALL ENTERS('OPINCR',*9999)

      CALL EXITS('OPINCR')
      RETURN
 9999 CALL ERRORS('OPINCR',ERROR)
      CALL EXITS('OPINCR')
      RETURN 1
      END


