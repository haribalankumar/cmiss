      SUBROUTINE OPMESH6(ERROR,*)

C#### Subroutine: OPMESH6
C###  Description:
C###    OPMESH6 outputs open cylindrical mesh.

      IMPLICIT NONE
!     Parameter List
      CHARACTER ERROR*(*)
!     Local Variables

      CALL ENTERS('OPMESH6',*9999)

      CALL EXITS('OPMESH6')
      RETURN
 9999 CALL ERRORS('OPMESH6',ERROR)
      CALL EXITS('OPMESH6')
      RETURN 1
      END


