      SUBROUTINE BMG2_SymStd_UTILS_dcopy( x, y, Nx, Ny )

C ==========================================================================
C
C   BMG2_SymStd_dcopy.f
C
C   --------------------
C   DESCRIPTION:
C   --------------------
C
C   BMG2_SymStd_dcopy.f replaces y with x.
C
C   ---------------------
C   HISTORY:  
C   ---------------------
C   
C   Written:    2003/07/03 (TMA)
C
C
C ==================================================================
C   INPUT:
C ========================
C
C
C
C ==================================================================
C   OUTPUT:
C ===========================
C
C
C
C ==================================================================
C   LOCAL:
C ========================
C
C
C
C ==========================================================================

      IMPLICIT NONE

C -----------------------------
C     Includes
C
      INCLUDE 'BMG_constants.h'

C ----------------------------
C     Argument Declarations
C 
      INTEGER  Nx, Ny
      REAL*8   x(Nx,Ny), y(Nx,Ny)

C ----------------------------
C     Local Declarations
C
      INTEGER  i, j

C =========================================================================

      DO j = 2, Ny-1
         DO i = 2, Nx-1
            y(i,j)=x(i,j)
         END DO
      END DO


C =========================================================================

      RETURN
      END
